#include "libft.h"

int		ft_is_prime(int nb)
{
	int i;

	i = 2;
	if (nb <= 1 || nb == 4)
		return (0);
	while (i < nb / 2 && i < 46341)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
