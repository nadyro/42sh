#include "libft.h"

int		ft_sqrt(int nb)
{
	int	i;
	int	result;

	i = 0;
	result = 0;
	if (nb < 0 || nb > 2147483647)
		return (0);
	while (nb >= 0 && nb <= 2147483647 && i < 46341)
	{
		result = i * i;
		if (nb == result)
			return (i);
		if (nb < i)
			return (0);
		i++;
	}
	return (0);
}
