#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*mem;

	mem = s1;
	while (*mem)
		mem++;
	while (*s2 && n--)
		*(mem++) = *(s2++);
	*mem = '\0';
	return (s1);
}
