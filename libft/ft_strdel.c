#include "libft.h"

void	ft_strdel(char **as)
{
	if (as != NULL)
	{
		if (*as != NULL)
		{
			ft_memset(*as, '\0', ft_strlen(*as));
			free((void *)*as);
			*as = NULL;
		}
	}
}
