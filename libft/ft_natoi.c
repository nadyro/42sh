#include "libft.h"

int	ft_natoi(char *s, int len)
{
	int i;
	int r;
	int n;

	if (!s)
		return (0);
	r = 0;
	n = s[0] == '-' ? 1 : 0;
	i = n;
	while (i < len && ft_isdigit(s[i]))
	{
		r = (r * 10) + s[i] - '0';
		i++;
	}
	r = n ? r * -1 : r;
	return (r);
}
